     private Vehiculo buscar_vehiculo(String placa){
        for(Vehiculo placaVehiculo : vehiculo){
           if(placaVehiculo.getPlaca().equals(placa))
               return placaVehiculo;
        }
        return null;
    }
    
    private void fnt_consultarEmpleado(String placa){
        Vehiculo placaVehiculo = buscar_vehiculo(placa);
        
        if(placaVehiculo != null){
            txt_nombre.setText(placaVehiculo.getNombre());
            txt_conductor.setText(placaVehiculo.getConductor());
            txt_ruta.setText(placaVehiculo.getRuta());
            txt_marca.setText(placaVehiculo.getMarca());
            txt_modelo.setText(placaVehiculo.getModelo());
            txt_descripcion.setText(placaVehiculo.getDescripsion()); 
        }
        else{
            JOptionPane.showMessageDialog(null, "No se encontro ningun vehiculo con ese numero de Placa", "ERROR" ,JOptionPane.ERROR_MESSAGE);
        }
    }    
    
