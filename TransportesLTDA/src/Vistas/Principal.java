     private void fnt_registrar(String placa, String nombre, String conductor, String ruta, String marca, String modelo, String descripsion){
        boolean identificacionRepetida = false;
        
        for(Vehiculo empleadoRepetido : vehiculo){
            if(empleadoRepetido.getPlaca().equals(placa)){
                identificacionRepetida = true;
                break;
            }
        }
        
        if(identificacionRepetida){
            JOptionPane.showMessageDialog(null, "El numero de placa ya esta registrado","ERROR" ,JOptionPane.ERROR_MESSAGE);           
        }else if(placa.isEmpty() || nombre.isEmpty() || conductor.isEmpty() || ruta.isEmpty() || marca.isEmpty() || modelo.isEmpty() || descripsion.isEmpty()){
        JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
        vehiculo.add(new Vehiculo(placa,nombre,conductor,ruta,marca,modelo,descripsion));
        JOptionPane.showMessageDialog(null, "Registrado correctamente" , "Registro", JOptionPane.PLAIN_MESSAGE);  
        fnt_limpiar();
        }
        
    }
    
    
    
    
